public class type {
    public static void main(String args[]){
        //숫자를 문자로 변환
        int num = 3;
        char ch = (char)(num + '0');
        System.out.println(ch);
        //문자를 숫자로 변환
        System.out.println(ch-'0'+1);
        //숫자를 문자열로 변환
        System.out.println(3+""+1);







    }
}
